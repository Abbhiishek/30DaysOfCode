decimal= int(input("Enter a decimal number: ")) # Get the decimal number by user input

print("The decimal value of", decimal, "is:") # Print the decimal value given by the user

print(bin(decimal), "in binary.") # decimal to binary

print(oct(decimal), "in octal.") # decimal to octal

print(hex(decimal), "in hexadecimal.") # decimal to hexadecimal
