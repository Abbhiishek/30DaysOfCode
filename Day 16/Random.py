import random                   # Import random module
print(random.randint(1,20))     # Random number between 1 and 20
print(random.randrange(1,100))  # Random number between 1 and 100
print(random.random())          # Random number between 0 and 1 but in decimal form
print(random.uniform(1,50))     # Random number between 1 and 50 but in decimal form
